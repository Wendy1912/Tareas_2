public class Conversiones {
    public double Centigrados ( double x )

  {         // Abre metodo Centigrado
  return ((5.0)/(9.0)) * ( x - 32 );
  }         // Cierra metodo Centigrado
  
  public double Fahrenheit( double y )

  {         // Abre metodo Centigrado
  return ((9.0)/(5.0)) *  y + 32 ;
  }   
}
