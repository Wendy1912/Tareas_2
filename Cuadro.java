public class Cuadro

{ // Abre clase Cuadro

    public void Cuadrado(int n)

    { // Abre metodo Cuadrado

        for (int i = 1; i <= n; i++) { // Abre for
            for (int j = 1; j <= n; j++)
                System.out.print("*");
            System.out.println();
        }
    } // Cierra metodo Cuadrado
} // Cierra clase Cuadro